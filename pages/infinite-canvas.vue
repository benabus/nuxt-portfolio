    <h2>Introduction</h2>

    <p>
        Comics, as most people think about them, have been around since the late
        1700s. They range from short political cartoons to super hero comic
        books to graphic novels.
    </p>
    <p>
        Comics is, however, a much more diverse medium. Influential comic book
        writer and artist Will Eisner defined comics as “sequential art” . Scott
        McCloud, a highly respected comic theorist, expands this with his own
        definition of “juxtaposed pictorial and other images in deliberate
        sequence”. Under these definitions, the word “comics” can be applied to
        early cave paintings and Egyptian hieroglyphics.
    </p>
    <p>
        The most recent format for comics is the web comic. Web comics are, as the
        name implies, comics that are posted on the web. While web comics can be
        traced back to Usenet, they became popular in the late 1990s with the
        start of such comics as Penny Arcade and PVP.
    </p>
    <p>
        Until recently, comics were confined to the physical dimensions of the
        medium that the comic is created in. In 1999, McCloud introduced the
        concept of what is commonly known as the Infinite Canvas . Computer
        screens could be thought of as windows that could be used to view a
        comic, allowing panels to be placed in any direction and made in any
        size, only limited by the creator’s imagination. Comics could be
        virtually limitless.
    </p>
    <p>
        Unfortunately, with the exception of a few experimental comics, the
        infinite canvas never took off. McCloud attributes this to both
        technical and mental limitations. At the time, screen sizes were
        limited. Broadband access was rare and expensive. User mental models
        were still stuck in “Web 1.0” where pages were static and literally used
        as pages, linking to other pages.
    </p>
    <p>
        Web comics utilized this ‘page paradigm’ and more or less remediated
        comic books and comic strips onto the web. Aside from the few “mad
        scientist experiments” , web comics did not take advantage of their new
        medium and the Infinite Canvas was forgotten.
    </p>
    <p>
        Today, we are in the midst of Web 2.0. Web 2.0 is defined by dynamic
        sites, social media, and rich internet apps. There is no reason that the
        Infinite Canvas cannot take advantage of these traits and become a rich,
        dynamic, social experience. It could be resurrected as an Infinite
        Canvas 2.0, if you will.
    </p>
