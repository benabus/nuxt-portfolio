<h2>About</h2>

<p>The Stache was created in 2009 using RPG Maker VX for an Interactive Storytelling course at Indiana University.</p>

<p>Its story revolves around a man named Barry who wakes up with his award winning mustache missing from his face.  Barry must go around town, interacting with the citizens, trying to figure out what happened to the 'stache.  Along the way, he will meet various famous characters and collect their mustaches which imbue him with their powers.</p>

<aside class="card float-md-left mb-3 mr-md-3 col-12 col-md-4 p-3">
    <p><strong>Protip</strong></p>
    <p>The game's theme is about accepting who you really are, so the less you wear a mustache, the stronger you get!</p>
</aside>

<h2>Development</h2>

<p>The adventure-game style game play was inspired by point-and-click games such as Leisure Suit Larry.  The "Mustache System" was inspired by Mega Man.  The turn-based battles were inspired by RPG Maker's built-in battle system.</p>

<p>RPG Maker VX was chosen because the lead developer (me) was somewhat familiar with the engine at the time and given the timespan of 4 months, this allowed the team to focus on story telling and game play, rather than learning a new engine.</p>

<p>Although CJ Page is credited as Producer, this was mostly a joke because the only thing he produced was moral support.  For the most part, as lead developer and designer, it fell on me to do much of the project management.</p>


